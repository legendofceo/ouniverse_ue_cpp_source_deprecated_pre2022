void ACompanionActor::Become(UCompanion* MCompanion)
{
	Companion = MCompanion;
	EmbodyComplete();
}

void ACompanionActor::EmbodyComplete()
{
	OnBecomeComplete.Broadcast();
}

void ACompanionActor::BeginEngage()
{
	AAppController::GetAppController(this)->Possess(this);
	BeginEngageBP();
}



void ACompanionActor::MoveForward(float Value)
{
	if ((Controller != NULL) && (Value != 0.0f))
	{
		// find out which way is forward
		const FRotator Rotation = Controller->GetControlRotation();
		const FRotator YawRotation(0, Rotation.Yaw, 0);

		// get forward vector
		const FVector Direction = FRotationMatrix(YawRotation).GetUnitAxis(EAxis::X);
		AddMovementInput(Direction, Value);
	}
}


void ACompanionActor::MoveRight(float Value)
{
	if ((Controller != NULL) && (Value != 0.0f))
	{
		// find out which way is right
		const FRotator Rotation = Controller->GetControlRotation();
		const FRotator YawRotation(0, Rotation.Yaw, 0);

		// get right vector 
		const FVector Direction = FRotationMatrix(YawRotation).GetUnitAxis(EAxis::Y);
		// add movement in that direction
		AddMovementInput(Direction, Value);
	}
}
